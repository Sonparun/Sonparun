sum=str(input('Enter a string: '))


print('This is what I found about hat string:')
if sum.isalnum():
    print('The string is alphanumberic.')
if sum.isalpha():
    print('The string contain only alphabetic characters.')   
if sum.isnumeric():
    print('The string contains only digits')
if sum.islower():
    print('The letters in the string are all lowercase')
if sum.isupper():
    print('The letters in the string are all uppercase')












